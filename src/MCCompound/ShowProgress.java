    @Override
    public void run() {
        int seconds = 180;
        String initial_title = getTitle();
        while (complete == false && seconds >= 0) {
            try {
                Thread.sleep(1000);
            } catch (InterruptedException ex) {
                Logger.getLogger(ShowProgress.class.getName()).log(Level.SEVERE, null, ex);
            }
            //
            seconds--;
            //
            this.setTitle(initial_title + " " + secondsToMiniutesAndSeconds(seconds));
        }
        //
        setTitle(completeMsg);
        //
    }
