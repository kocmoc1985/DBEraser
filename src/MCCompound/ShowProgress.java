    private String secondsToMiniutesAndSeconds(int seconds) {
        int sec = seconds % 60;
        int min = (seconds - sec) / 60;
        if (sec < 10) {
            return min + ":0" + sec;
        } else {
            return min + ":" + sec;
        }
    }

